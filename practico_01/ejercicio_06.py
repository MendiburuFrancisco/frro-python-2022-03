    listaOrdenadaDeEnteros = filter(lambda l: type(l) == int,lista)
    listaOrdenadaDeString = filter(lambda l: type(l) == str,lista)
    
    return list(listaOrdenadaDeString) + list(listaOrdenadaDeEnteros)


